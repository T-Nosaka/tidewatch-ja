/*
 * 潮汐取得
 */
function Get_Tide(Tideyy, Tidemm, Tidedd, Tidehh, Tidejisa, siteinfo )
{
  // 年月日時刻、時差（日本時は+9時）
  // 指定日時の潮高を計算する
  // OmitFlag = true の場合は因数計算等の一部を省略（高速化）
  var d2000, i;
  var Tide_s,Tide_h,Tide_p,Tide_N,Tide_ps;
  var Vi = new Array(40);
  var Hi = new Array(40);

    //経過日数計算
    d2000 = GetJ2000Days(Tideyy, Tidemm, Tidedd);
    Tide_s = Get_s(d2000);
    Tide_h = Get_h(d2000);
    Tide_p = Get_p(d2000);
    Tide_N = Get_N(d2000);
    Tide_ps = Get_ps(d2000);

    //調和定数　潮汐ポテンシャル??
    Set_FiUi(Tide_p, Tide_N); // fi,ui の初期化

    // 観測点の標準時 0時における天文引数 Vi+Ui → Vi[i],振幅 Hi*fi → Hi[i]
    for (i = 0; i < 40; i++) {
      if (siteinfo.H1[i] != 0) {
        Vi[i] = marume360( Get_Voi(i, Tide_s, Tide_h, Tide_p) + Ui[i] - Buncyo[i][0] * -siteinfo.lon + Buncyo[i][1] * ( -Tidejisa) - siteinfo.K1[i] );
        Hi[i] = siteinfo.H1[i] * Fi[i];
      } else {
        Hi[i] = 0;
        Vi[i] = 0;
      }
    }

  var TideHight = siteinfo.Z0;
  for (i = 0; i < 40; i++) {
    if (Hi[i] != 0) {
      TideHight += Hi[i] * Math.cos((Vi[i] + Buncyo[i][1] * Tidehh) * d2r);
    }
  }
  return TideHight; // 潮高を返す
}


/*
 * 2点間の距離 (Km)
 */
function DiffLength( plat1, plon1, plat2, plon2 )
{
  //赤道半径[km]
  var r = 6378.137;

  // A点
  var lat1 = plat1 * Math.PI / 180.0;
  var lng1 = plon1 * Math.PI / 180.0;

  // B点
  var lat2 = plat2 * Math.PI / 180.0;
  var lng2 = plon2 * Math.PI / 180.0;

  // 2点間の距離[km]
  return r * Math.acos(Math.sin(lat1) * Math.sin(lat2) + Math.cos(lat1) * Math.cos(lat2) * Math.cos(lng2 - lng1));
}

